// @lc code=start
/**
 * @param {number} m
 * @param {number} n
 * @return {number}
 */
var uniquePaths = function(m, n) {
    const dp = Array(m + 1).fill(0).map(v => Array(n + 1).fill(0))

    function recursive(x, y){
        if(x > m || y > n) return 0
        if(x === m && y === n) return 1
        if(dp[x][y]) return dp[x][y]
        dp[x][y] = recursive(x + 1, y) + recursive(x, y + 1)

        return dp[x][y]
    }

    return recursive(1, 1)
};
